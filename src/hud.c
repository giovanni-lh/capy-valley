void hud_init(void) {
    BG_PALETTE[16] = RGB15(0, 0, 0);
    BG_PALETTE[17] = RGB15(31, 31, 31);

    volatile u32 *cbb1 = BG_CHARBLOCK(1);
    for (int g = 0; g < 11; g++) {
        volatile u32 *dst = cbb1 + (g + 1) * 8;
        for (int r = 0; r < 8; r++) {
            dst[r] = font_tiles[g][r];
        }
    }
}

void hud_update(const Clock *c) {
    int hour = clock_hour(c);
    int min = clock_min(c);

    volatile u16 *sbb = BG_SCREENBLOCK(30);
    sbb[0] = HUD_TILE(1 + hour / 10);
    sbb[1] = HUD_TILE(1 + hour % 10);
    sbb[2] = HUD_TILE(11);
    sbb[3] = HUD_TILE(1 + min / 10);
    sbb[4] = HUD_TILE(1 + min % 10);
}