static void fill_screen(uint16_t color) {
    volatile uint16_t *ptr = VRAM;
    for (int i = 0; i < SCREEN_W * SCREEN_H; i++) {
        ptr[i] = color;
    }
}

static void draw_pixel(int x, int y, uint16_t color) {
    if ((unsigned int)x < SCREEN_W && (unsigned int)y < SCREEN_H) {
        VRAM[y * SCREEN_W + x] = color;
    }
}

static void draw_char(int x, int y, char c, uint16_t fg, uint16_t bg) {
    if (c < 0x20 || c > 0x7E) return; // Only draw printable characters
    const uint8_t *glyph = font8x8[(unsigned char)c - 0x20];
    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            draw_pixel(x + col, y + row, (bits & (0x80 >> col)) ? fg : bg);
        }
    }
}

static void draw_text(int x, int y, const char *str, uint16_t fg, uint16_t bg) {
    for (int i = 0; str[i]; i++) {
        draw_char(x + i * 8, y, str[i], fg, bg);
    }
}

